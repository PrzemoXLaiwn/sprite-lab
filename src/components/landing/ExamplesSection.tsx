const FILTERS = [
  { id: "all", label: "All", icon: Sparkles },
  { id: "Weapons", label: "Weapons", icon: Swords },
  { id: "Armor", label: "Armor", icon: Shield },
  { id: "Characters", label: "Characters", icon: Users },
  { id: "Creatures", label: "Creatures", icon: PawPrint },
  { id: "Items", label: "Items", icon: Key },
  { id: "Consumables", label: "Consumables", icon: FlaskConical },
  { id: "Resources", label: "Resources", icon: Gem },
];

export function ExamplesSection() {
  const [filter, setFilter] = useState("all");
  const [imageErrors, setImageErrors] = useState<Record<number, boolean>>({});

  const filteredAssets = filter === "all"
    ? EXAMPLE_ASSETS
    : EXAMPLE_ASSETS.filter(asset => asset.category === filter);

  return (
    <section id="examples" className="py-20">
      <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
        <div className="text-center mb-12">
          <div className="inline-flex items-center gap-2 px-4 py-2 rounded-full bg-primary/10 text-primary text-sm font-medium mb-4">
            <Sparkles className="w-4 h-4" />
            Community Showcase
          </div>
          <h2 className="text-3xl font-bold mb-4">
            See What You Can Create
          </h2>
          <p className="text-muted-foreground max-w-2xl mx-auto">
            Real examples generated by SpriteLab. Each asset was created in seconds, not hours.
          </p>
        </div>

        {/* Filters */}
        <div className="flex flex-wrap justify-center gap-2 mb-8">
          {FILTERS.map((f) => {
            const Icon = f.icon;
            return (
              <button
                key={f.id}
                onClick={() => setFilter(f.id)}
                className={`px-4 py-2 rounded-full text-sm font-medium transition-all flex items-center gap-2 ${
                  filter === f.id
                    ? "bg-primary text-primary-foreground"
                    : "bg-card border border-border text-muted-foreground hover:text-foreground hover:border-primary/50"
                }`}
              >
                <Icon className="w-4 h-4" />
                {f.label}
              </button>
            );
          })}
        </div>

        {/* Gallery Grid */}
        <div className="grid grid-cols-2 sm:grid-cols-3 lg:grid-cols-4 gap-4">
          {filteredAssets.map((asset) => (
            <div
              key={asset.id}
              className="group relative bg-card border border-border rounded-xl overflow-hidden hover:border-primary/50 transition-all hover:shadow-lg hover:shadow-primary/10"
            >
              {/* Image */}
              <div className="aspect-square bg-muted/30 relative overflow-hidden">
                {!imageErrors[asset.id] ? (
                  <img
                    src={asset.image}
                    alt={asset.prompt}
                    className="w-full h-full object-contain p-4 group-hover:scale-110 transition-transform duration-300"
                    onError={() => setImageErrors(prev => ({ ...prev, [asset.id]: true }))}
                  />
                ) : (
                  <div className="w-full h-full flex items-center justify-center text-6xl">
                    {asset.fallbackEmoji}
                  </div>
                )}

                {/* 3D Badge */}
                {asset.is3D && (
                  <div className="absolute top-2 left-2 px-2 py-1 rounded-full bg-[#c084fc]/90 text-white text-xs font-bold flex items-center gap-1">
                    <Cuboid className="w-3 h-3" />
                    3D
                  </div>
                )}

                {/* 2D Badge */}
                {!asset.is3D && (
                  <div className="absolute top-2 left-2 px-2 py-1 rounded-full bg-[#00ff88]/90 text-[#030305] text-xs font-bold flex items-center gap-1">
                    <ImageIcon className="w-3 h-3" />
                    2D
                  </div>
                )}

                {/* Hover Overlay */}
                <div className="absolute inset-0 bg-black/70 opacity-0 group-hover:opacity-100 transition-opacity flex items-center justify-center">
                  <div className="text-center p-4">
                    <p className="text-sm text-white font-medium mb-2">&quot;{asset.prompt}&quot;</p>
                    <div className="flex items-center justify-center gap-2">
                      <span className="text-xs px-2 py-1 rounded-full bg-primary/20 text-primary">
                        {asset.style}
                      </span>
                    </div>
                  </div>
                </div>
              </div>

              {/* Info */}
              <div className="p-3">
                <p className="text-xs text-muted-foreground truncate">
                  {asset.style} ‚Ä¢ {asset.category}
                </p>
              </div>
            </div>
          ))}
        </div>

        {/* CTA */}
        <div className="text-center mt-12">
          <p className="text-muted-foreground mb-4">
            Ready to create your own game assets?
          </p>
          <a
            href="/register"
            className="inline-flex items-center gap-2 px-6 py-3 rounded-lg bg-gradient-to-r from-primary to-purple-500 text-primary-foreground font-medium hover:opacity-90 transition-opacity"
          >
            <Sparkles className="w-4 h-4" />
            Start Creating Free
          </a>
        </div>
      </div>
    </section>
  );
}
